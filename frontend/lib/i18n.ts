export function t(key: string, lang: Lang = getLang()): string {
  return labels[lang][key] ?? labels.en[key] ?? key;
}

export function tf(key: string, vars: Record<string, string | number>, lang: Lang = getLang()): string {
  let text = t(key, lang);
  for (const [k, v] of Object.entries(vars)) {
    text = text.replace(`{${k}}`, String(v));
  }
  return text;
}

export function tStatus(status: string, lang: Lang = getLang()): string {
  const key = `status${status.replace(/\s+/g, '_')}`;
  return labels[lang][key] ?? labels.en[key] ?? status;
}
