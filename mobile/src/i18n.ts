let currentLang: Lang = 'en';
let langListeners: Array<(lang: Lang) => void> = [];

export function setLang(lang: Lang) {
  currentLang = lang;
  langListeners.forEach((fn) => fn(lang));
}

export function getLang(): Lang {
  return currentLang;
}

export function subscribeLang(fn: (lang: Lang) => void) {
  langListeners.push(fn);
  return () => {
    langListeners = langListeners.filter((l) => l !== fn);
  };
}

export function t(key: string): string {
  return labels[currentLang][key] ?? labels.en[key] ?? key;
}

export function tStatus(status: string): string {
  const key = `status${status.replace(/\s+/g, '_')}`;
  return labels[currentLang][key] ?? labels.en[key] ?? status;
}
